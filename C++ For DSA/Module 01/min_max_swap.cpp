#include<iostream>
#include<algorithm>
using namespace std;

int main() 
{
    // Min Function
    cout << min({2, 4, 4, 7, 5, 3, 3 , 1, 8, 4}) << endl;

    // Max Function
    cout << max({2, 4, 4, 7, 5, 3, 3 , 1, 8, 4}) << endl;
    
    // Swap Function
    int a = 5, b = 10;
    swap(a, b);
    cout << a << " " << b << endl;

    return 0;
}